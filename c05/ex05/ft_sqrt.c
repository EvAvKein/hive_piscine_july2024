int	ft_sqrt(int nb)
{
	int	i;

	if (nb < 1)
		return (0);
	if (nb == 1)
		return (1);
	i = 0;
	while (i < nb)
	{
		if ((i * i) == nb)
			return (i);
		i++;
	}
	return (0);
}
